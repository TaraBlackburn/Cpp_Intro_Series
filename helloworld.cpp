#include <iostream>
// Instantiate libraries just like in Python called pre processor directive:
// input output stream = "i o stream"
int main() {
  
  std::cout << "Hello World!\n";
  // std::cout = standard character out - what they call a "c out" 
  // "<<" that is a pointer 
  // "\n" denoting a new line
  // ";" ending the statement 
  std::cout << "Welcome to My C++ Series\n";

  return 0;
}

